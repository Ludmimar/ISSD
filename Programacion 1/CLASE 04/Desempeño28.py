# Desarrollar un programa que muestre la tabla de multiplicar del 5
# (del 5 al 50)

for f in range(1,11):
    c = f*5
    print(c)
    
# La función range puede tener también tres parámetros,
# el primero indica el valor inicial que tomará la variable x,
# el segundo parámetro el valor final (que no se incluye) y
# el tercer parámetro indica cuanto se incrementa cada vuelta x.    
for f in range(5,51,5):
    print(f)
