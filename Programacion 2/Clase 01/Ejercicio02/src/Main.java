
public class Main {
    public static void main(String[] args) {
        byte edad = 12;
        short largo = 10000;
        int ancho = 2000000000;
        long distancia = 700000000000000000L;
        float peso1 = 50.25F;
        double peso2 = 67.45;
        boolean existe = true;
        char letra = 'A';
        String nombre = "Rodriguez Pablo";

        System.out.println("byte: " + edad);
        System.out.println("short: " + largo);
        System.out.println("int: " + ancho);
        System.out.println("long: " + distancia);
        System.out.println("float: " + peso1);
        System.out.println("double: " + peso2);
        System.out.println("boolean: " + existe);
        System.out.println("char: " + letra);
        System.out.println("String: " + nombre);
    }
}